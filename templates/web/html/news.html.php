        <p class="publication-objective">
        Thomas, un jeune diplômé en informatique, face à une pile de candidatures pour un stage dans une start-up très convoitée.
         Dépassé par la concurrence, il décide d'explorer une nouvelle voie : l'intelligence artificielle. 
         En quelques clics, il génère des dizaines de CV optimisés pour chaque poste, mettant en avant les compétences 
         les plus pertinentes et les mots-clés recherchés par les recruteurs.
         Résultat ? Il décroche le stage tant convoité, laissant derrière lui des concurrents plus expérimentés mais moins 
         agiles.
        </p>
