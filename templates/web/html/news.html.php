            <div id="part1">
                <h2>Pourquoi l'IA est votre allié pour rédiger votre CV ?</h2>
               
                <li>Gain de temps considérable : Fini les heures passées à peaufiner chaque phrase.
                     L'IA génère des brouillons de qualité en quelques secondes, vous laissant le temps
                      de vous concentrer sur le fond.
                </li>
                <li>
                Personnalisation à grande échelle : Chaque offre d'emploi est unique. 
                L'IA adapte votre CV en fonction des mots-clés
                 et des compétences recherchées, augmentant ainsi vos chances de passer 
                 les filtres des ATS (Applicant Tracking Systems)
                </li>
                <li>
                Amélioration de la formulation : L'IA utilise des algorithmes sophistiqués pour 
                vous aider à rédiger des phrases claires, concises et percutantes, mettant en valeur vos atouts
                </li>
            </div>
            <div id="part2">
                <h2>Comment utiliser l'IA pour rédiger un CV efficace ?</h2>
                <ol>
                    <li>Fournir des informations précises : Plus vous donnez de détails sur votre parcours, 
                        vos compétences et vos objectifs, plus l'IA pourra générer un CV personnalisé.
                    </li>
                    <li>Choisir un modèle adapté : De nombreux outils proposent différents modèles de CV. 
                        Optez pour celui qui correspond le mieux à votre secteur d'activité et à votre expérience
                    </li>
                    <li>Personnaliser et vérifier : L'IA est un outil, pas un magicien. Relisez attentivement le CV 
                        généré et apportez votre touche personnelle pour qu'il reflète votre personnalité
                    </li>

                    <li>
                    Utiliser l'IA comme un tremplin : L'IA vous aide à démarrer, mais c'est à vous de donner vie à votre 
                    CV en y ajoutant votre expertise et votre passion
                    </li>
                    
                </ol>
            </div>
